package session3;

public class TernaryDemo {

    public static void main(String[] args) {
        // 6 Ternary operators
        int a = 10, b = 20;
        int max = (a > b) ? a : b;
        System.out.println("Max value using ternary (a > b) ? a : b: " + max + " (returns a if a > b, otherwise b)");
    }
}
