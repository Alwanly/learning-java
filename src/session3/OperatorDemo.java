package session3;

public class OperatorDemo {

    public static void main(String[] args) {
        
        // 1 Arithmetic operators
        int a = 10, b = 20;
        System.out.println("Value of a: " + a + " (first operand)");
        System.out.println("Value of b: " + b + " (second operand)");
        System.out.println("Addition (a+b): " + (a + b) + " (sum of both operands)");
        System.out.println("Subtraction (a-b): " + (a - b) + " (difference between a and b)");
        System.out.println("Multiplication (a*b): " + (a * b) + " (product of both operands)");
        System.out.println("Division (a/b): " + (a / b) + " (quotient when a is divided by b)");
        System.out.println("Modulus (a%b): " + (a % b) + " (remainder when a is divided by b)");
    
    }
}
