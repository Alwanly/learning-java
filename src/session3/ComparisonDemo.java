package session3;

public class ComparisonDemo {
    public static void main(String[] args) {

        // 2 Comparison operators
        int a = 10, b = 20;
        System.out.println("Equal to (a==b): " + (a == b) + " (checks if a equals b)");
        System.out.println("Not equal to (a!=b): " + (a != b) + " (checks if a is not equal to b)");
        System.out.println("Greater than (a>b): " + (a > b) + " (checks if a is greater than b)");
        System.out.println("Less than (a<b): " + (a < b) + " (checks if a is less than b)");
        System.out.println(
                "Greater than or equal to (a>=b): " + (a >= b) + " (checks if a is greater than or equal to b)");
        System.out.println("Less than or equal to (a<=b): " + (a <= b) + " (checks if a is less than or equal to b)");

    }
}
