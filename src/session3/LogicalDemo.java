package session3;

public class LogicalDemo {

    public static void main(String[] args) {
        // 3 Logical operators
        boolean x = true, y = false;
        System.out.println("Value of x: " + x + " (first boolean operand)");
        System.out.println("Value of y: " + y + " (second boolean operand)");
        System.out.println("Logical AND (x&&y): " + (x && y) + " (true only if both x and y are true)");
        System.out.println("Logical OR (x||y): " + (x || y) + " (true if either x or y is true)");
        System.out.println("Logical NOT (!x): " + (!x) + " (inverts the value of x)");
    }

}
